#INCLUDE <PIC18F452.INC>

#DEFINE BOTAO_RB3 PORTB,3

ORG Ox00
GOTO INICIO 

INICIO 
    CLRF PORTB
    CLRF PORTD 
    MOVLW B'00001000'
    MOVWF TRISB
         
MAIN 
    BTFSC BOTAO_RB3
    GOTO MAIN
    INCF PORTD,F
    MOVLW .4
    SUBWF PORTD,W
    BTFSC STATUS,Z
    GOTO MAIN
    GOTO LIMPA_PORTD 

LIMPA_PORTD 
    CLRF PORTD 
END 

